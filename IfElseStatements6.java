public class IfElseStatements6{
  
  public static void main(String[] args){
    boolean state = false;
    String utter;
    
    
    if ( state ){
      utter = new String(" state is true");
    }
    
    else{
      utter = new String(" state is false");
    }
    
    System.out.println(utter);
    
  }
}